#include <iostream>
#include <cmath>
using namespace std;

int main()
{
  // Square Root
  cout << "Square Root: " << sqrt(16) << endl; // 4

  // Power
  cout << "Power: " << pow(2, 3) << endl; // 2^3 = 8

  // Floor
  cout << "Floor: " << floor(2.6) << endl; // 2

  // Ceiling
  cout << "Ceiling: " << ceil(2.3) << endl; // 3

  // Absolute
  cout << "Absolute: " << abs(-10) << endl; // 10

  return 0;
}